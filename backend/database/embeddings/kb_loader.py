"""
Knowledge Base Loader
Loads and retrieves information from preprocessed embeddings.
Reads chunk and vector files generated by generate_embeddings.py
Can load from FAISS index, .pkl embeddings, or both.
"""

import os
import pickle
import glob
from generate_embeddings import EmbeddingProcessor


class KnowledgeBaseLoader:
    """
    Main class to load and query the knowledge base.
    Loads pre-generated embeddings (FAISS indices, .pkl files, and chunk text) 
    from files created by generate_embeddings.py
    """

    def __init__(self, model_name=None, output_dir=None):
        """Initialize KB loader with embedding processor and retriever
        
        Args:
            model_name: Name of sentence transformer model
            output_dir: Directory containing embeddings (default: output_embeddings in same folder)
        """
        if output_dir is None:
            output_dir = os.path.join(os.path.dirname(__file__), "output_embeddings")
        self.processor = EmbeddingProcessor(model_name=model_name, output_dir=output_dir)
        self.output_dir = output_dir

    def load_embeddings_pkl(self, save_name, verbose=True):
        """
        Load embeddings from .pkl file.
        
        Args:
            save_name: Base name of saved files (e.g., "ค่าเทอม_embedding_fees")
            verbose: Print progress
            
        Returns:
            numpy array of embeddings (float32)
        """
        pkl_path = os.path.join(self.output_dir, f"{save_name}_embeddings.pkl")
        
        if not os.path.exists(pkl_path):
            raise FileNotFoundError(f"ไม่พบไฟล์ embeddings: {pkl_path}")
        
        with open(pkl_path, "rb") as f:
            embeddings = pickle.load(f)
        
        if verbose:
            print(f"Loaded embeddings from {pkl_path}")
            print(f"Shape: {embeddings.shape}")
        
        return embeddings

    def load_chunks(self, save_name, verbose=True):
        """
        Load chunks from text file.
        
        Args:
            save_name: Base name of saved files (e.g., "ค่าเทอม_embedding_fees")
            verbose: Print progress
            
        Returns:
            List of chunk strings
        """
        chunks_path = os.path.join(self.output_dir, f"{save_name}_chunks.txt")
        
        if not os.path.exists(chunks_path):
            raise FileNotFoundError(f"ไม่พบไฟล์ chunks: {chunks_path}")
        
        with open(chunks_path, "r", encoding="utf-8") as f:
            raw = f.read().strip()
        
        chunks = [c.strip() for c in raw.split("\n---\n") if c.strip()]
        
        if verbose:
            print(f"Loaded {len(chunks)} chunks from {chunks_path}")
        
        return chunks

    def load_all(self, save_name, verbose=True):
        """
        Load all files (embeddings + chunks) for a knowledge base.
        
        Args:
            save_name: Base name of saved files
            verbose: Print progress
            
        Returns:
            Tuple of (embeddings, chunks)
        """
        embeddings = self.load_embeddings_pkl(save_name, verbose=verbose)
        chunks = self.load_chunks(save_name, verbose=verbose)
        
        return embeddings, chunks

    def list_available_embeddings(self):
        """List all available embedding sets in Final_output folder"""
        pkl_files = glob.glob(os.path.join(self.output_dir, "*_embeddings.pkl"))
        
        available = []
        for pkl_file in sorted(pkl_files):
            basename = os.path.basename(pkl_file)
            save_name = basename.replace("_embeddings.pkl", "")
            available.append(save_name)
        
        return available

    def load_embeddings(self, save_name, copy_to_ascii=None, verbose=True):
        """
        Load pre-generated embeddings and chunks from disk (FAISS index).
        
        Args:
            save_name: Base name of saved files (without extension)
                      e.g., "ค่าเทอม_embedding_fees"
            copy_to_ascii: Optional ASCII filename to copy .faiss to (workaround for Windows Thai names)
            verbose: Print progress
            
        Returns:
            Tuple of (index, chunks)
        """
        index, chunks = self.processor.load_index_and_chunks(
            save_name,
            copy_to_ascii=copy_to_ascii
        )
        if verbose:
            print(f"Loaded {len(chunks)} chunks from {save_name}")
        return index, chunks

    def query_teachers(self, query, top_k=5):
        """Query teacher information"""
        return self.retriever.search_teachers(query, top_k=top_k)

    def query_fees(self, query, top_k=5):
        """Query fee information"""
        return self.retriever.search_fees(query, top_k=top_k)

    def query_companies(self, query, top_k=5):
        """Query company/MOU information"""
        return self.retriever.search_companies(query, top_k=top_k)

    def query_all(self, query, top_k=5):
        """Query all knowledge bases"""
        return self.retriever.search_all(query, top_k=top_k)

    def print_results(self, results, query):
        """Pretty print search results"""
        print(f"\n{'='*60}")
        print(f"Query: {query}")
        print(f"{'='*60}")

        if isinstance(results, dict):
            # Results from search_all()
            for category, matches in results.items():
                print(f"\n{category.upper()}:")
                if matches:
                    for i, r in enumerate(matches, 1):
                        print(f"  [{i}] (distance: {r['distance']:.4f})")
                        print(f"      {r['chunk'][:150]}...")
                else:
                    print(f"  No results found")
        else:
            # Results from specific search (list)
            if results:
                for i, r in enumerate(results, 1):
                    print(f"[{i}] (distance: {r['distance']:.4f})")
                    print(f"    {r['chunk']}")
            else:
                print("No results found")


